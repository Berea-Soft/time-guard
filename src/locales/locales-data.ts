/**
 * Dynamically register all locales
 */
export function registerAllLocales(
  localeMap: Map<string, ILocale> | Record<string, ILocale>,
): void {
  if (localeMap instanceof Map) {
    Object.entries(LOCALES_DATA).forEach(([code, data]) => {
      localeMap.set(code, data);
    });
  } else {
    Object.entries(LOCALES_DATA).forEach(([code, data]) => {
      localeMap[code] = data;
    });
  }
}

/**
 * Get all available locales
 */
export function getAvailableLocales(): string[] {
  return Object.keys(LOCALES_DATA);
}

